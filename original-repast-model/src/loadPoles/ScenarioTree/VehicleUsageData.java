package loadPoles.ScenarioTree;

public class VehicleUsageData {	
	protected int bicycleUsage;
	protected int electricBicycleUsage;
	protected int motorUsage;
	protected int publicTransportUsage;
	protected int normalCarUsage;
	protected int hybridCarUsage;
	protected int electricCarUsage;
	
	public int getBicycleUsage() {
		return bicycleUsage;
	}
	
	public int getElectricBicycleUsage() {
		return electricBicycleUsage;
	}
	
	public int getMotorUsage() {
		return motorUsage;
	}
	
	public int getPublicTransportUsage() {
		return publicTransportUsage;
	}
	
	public int getNormalCarUsage() {
		return normalCarUsage;
	}
	
	public int getHybridCarUsage() {
		return hybridCarUsage;
	}
	
	public int getElectricCarUsage() {
		return electricCarUsage;
	}
}
